        <p>—Министр цифрового развития, инновации и аэрокосмической промышленности Багдат Мусин встретился с представителями компаний и стартапов IT-экспортеров, сообщает пресс-служба ведомства.

            В ходе встречи Багдат Мусин отметил важность экспорта IT-услуг и поднял важнейшие вопросы, связанные с развитием ИТ-рынка, а также совместно с участниками идентифицировал дальнейшие шаги отметив важность сотрудничества между государственным и частным секторами для дальнейшего устойчивого развития.
